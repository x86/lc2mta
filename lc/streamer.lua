function replaceModel ( iModel, sDFF, sTXD, pObject )
	local pTXD    = nil
	local bSucces = false

	-- We use a cache system now!
	if not cacheTextures [ sTXD ] then
		if fileExists ( "txd/" .. sTXD .. ".txd" ) then
			pTXD = engineLoadTXD ( "txd/" .. sTXD .. ".txd" )
			if not pTXD then
				outputDebugString ( tostring ( pTXD ) .. " => " .. sTXD )
			else
				cacheTextures [ sTXD ] = pTXD
			end
		end
	else
		pTXD = cacheTextures [ sTXD ]
	end
	
	if pTXD then
		if not engineImportTXD ( pTXD, iModel ) then
			-- Cant load the TXD
			if failReplace [ iModel ] == nil then
				-- Retry
				--outputDebugString ( "Cant load TXD of " .. iModel .. " (Reason: engineImportTXD returned false), retrying ...", 2 )
				failReplace [ iModel ] = true
				return replaceModel ( iModel, sDFF, sTXD )
			else
				outputChatBox ( "[WARNING]: " .. iModel .. " couldn't load the TXD after 2 times, check plis!" )
			end
		else
			-- We can load the TXD now after failing once!
			if failReplace [ iModel ] then
				-- But hey, we failed to load once, tell in chatbox that it fixed.
				--outputChatBox ( "Hey, " .. iModel .. " loaded his texture now, after 1 retry!" )
			end
		end
	else
		-- invalid pTXD pointer (probably going to be white!), retry.
		if failReplace [ iModel ] == nil then
			-- Retry
			--outputDebugString ( "Cant load TXD of " .. iModel .. " (Reason: false), retrying ...", 2 )
			failReplace [ iModel ] = true
			return replaceModel ( iModel, sDFF, sTXD )
		end
	end

	-- Replace the DFF and COL, first check if it exists.
	if fileExists ( "col/" .. sDFF .. ".col" ) then
	
		-- Load and replace the collision
		local pCOL = engineLoadCOL ( "col/" .. sDFF .. ".col" )
		if pCOL then
			engineReplaceCOL ( pCOL, iModel )
			--engineReplaceCOL ( pCOL, iModel ) -- avoid collisionless objects. -> Doesn't help.
		else
			--outputDebugString ( "[COL]: Cant _load_ the COL of " .. iModel .. " [" .. sDFF .. "] ...", 2 )
		end

		-- Load and replace the dff
		local pDFF = engineLoadDFF ( "dff/" .. sDFF .. ".dff", iModel )
		if pDFF then
			engineReplaceModel ( pDFF, iModel )
			--engineReplaceModel ( pDFF, iModel ) -- TEST #2: Avoid collisionless objects.
			
			bSucces = true
		else
			--outputDebugString ( "[DFF]: Cant _load_ the DFF of " .. iModel .. " [" .. sDFF .. "] ...", 2 )
		end
	else
		-- Just ignore it ...
		--outputDebugString ( "Cant load the DFF/COL of model " .. iModel .. " [" .. sDFF .. "] - 404 ...", 2 )
	end
	
	-- Enable col and make it visible
	--[[if bSucces then
		setElementCollisionsEnabled ( pObject, true )
		setElementAlpha ( pObject, 255 )
	end]]
	
	__updateCache ( )
end

function __updateCache ( )
	-- Clean the cache
	iReplaced    = 0
	iFailReplace = 0
	iCached      = 0
	
	-- Update replaced amount
	for _, i in pairs ( replaced ) do
		iReplaced = iReplaced + 1
	end

	-- Update fail replacements (textures)
	for _, i in pairs ( failReplace ) do
		iFailReplace = iFailReplace + 1
	end

	-- Cached textures
	for _, i in pairs ( cacheTextures ) do
		iCached = iCached + 1
	end
end

function checkIfUnwantedObject ( pObject, sDFF )
	if string.sub ( sDFF, 0, 3 ) == "bed" then -- no sea beds
		destroyElement ( pObject )
		return true
	end
	
	if string.sub ( sDFF, 0, 6 ) == "sbsbed" then -- no sea beds
		destroyElement ( pObject )
		return true
	end
	
	if string.find ( sDFF, "treeshad" ) then -- no tree shadows
		destroyElement ( pObject )
		return true
	end
	
	return false
end

